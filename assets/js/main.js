

$(document).ready(function(){

    $('#openBar').click(function(){
        $('#sidebar').show();
        
    })

    $('#closeBar').click(function(){
        $('#sidebar').hide();
       
    })
    
    $('#moreText').click(function(){
        $('#extraText').toggle();
       
        
    })
    $('#play').click(function(){
        $('#video').show();
        
    })
    $('#close').click(function(){
        $('#video').hide();
        
    })
    
    }



)




    
